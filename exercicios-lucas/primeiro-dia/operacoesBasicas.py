# Operações básicas em Python

# Declaração de variáveis
num_a = 10  # Tipo: int -> Número inteiro.
num_b = 2.5  # Tipo: float -> Número com casa decimal.

# Mensagem de resultado.
mensagem_resultado = "Resultado das operações: "  # Tipo: string -> Texto.

print(mensagem_resultado)
# Operações matemáticas
soma = num_a + num_b # Tipo: float -> Número com casa decimal.
print(f"Soma: {soma}")

subtracao = num_a - num_b # Tipo: float -> Número com casa decimal.
print(f"Substração: {subtracao}")

multiplicacao = num_a * num_b # Tipo: float -> Número com casa decimal.
print(f"Multiplicação: {multiplicacao}")

divisao = num_a / num_b # Tipo: float -> Número com casa decimal.
print(f"Divisão: {divisao}")
